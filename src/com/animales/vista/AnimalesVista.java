    private void BotonConsultar(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonConsultar
        
        int fila = jTable.getSelectedRow();
        if (fila>=0){
            jTable.getValueAt(fila, 3).toString();
            jTextFieldID.setText(jTable.getValueAt(fila, 0).toString());
            jTextFieldRaza.setText(jTable.getValueAt(fila, 1).toString());
            jTextFieldNombre.setText(jTable.getValueAt(fila, 2).toString());
            jTextFieldEdad.setText(jTable.getValueAt(fila, 3).toString());
            jTextFieldGenero.setText(jTable.getValueAt(fila, 4).toString());
            jTextFieldAlimentacion.setText(jTable.getValueAt(fila, 5).toString());
        }else{
            JOptionPane.showMessageDialog(null, "Fila no seleccionada");
        }
        
    }//GEN-LAST:event_BotonConsultar

