    public void actualizarTabla() {
        String[] titulos = {"ID", "RAZA", "NOMBRE", "EDAD", "GENERO", "ALIMENTACIÓN"};

        DefaultTableModel dt = new DefaultTableModel(null, titulos);
        List<AnimalesModel> male = controlador.getListaAnimales();
        String[] fila = new String[6];

        for (AnimalesModel animal : male) {
            fila[0] = String.valueOf(animal.getIdAnimal());
            fila[1] = animal.getRaza();
            fila[2] = animal.getNombre();
            fila[3] = String.valueOf(animal.getEdad());
            fila[4] = animal.getGenero();
            fila[5] = animal.getAlimentacion();
            dt.addRow(fila);
        }
        jTable.setModel(dt);
    }

